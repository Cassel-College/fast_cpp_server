#include "example.h"

Example::Example() {}

/**
 * @brief A simple function that adds two numbers.
 * @param a First number.
 * @param b Second number.
 * @return The sum of a and b.
 */
int Example::add(int a, int b) {
    return a + b;
}

/**
 * @brief A simple function that adds two numbers.
 * @param a First number.
 * @param b Second number.
 * @return The subtract of a and b.
 */
int Example::subtract(int a, int b) {
    return a - b;
}